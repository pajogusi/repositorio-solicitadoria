<h1>Aula 03 - Escassez, Escolha e Custo de Oportunidade</h1>

<section class="box">
<h2>Objetivos da Aula</h2>
<ul>
<li>Compreender o conceito de escassez.</li>
<li>Explicar porque a escassez obriga à escolha.</li>
<li>Definir custo de oportunidade.</li>
<li>Aplicar estes conceitos ao quotidiano.</li>
<li>Relacionar decisões económicas com o Direito e a Solicitadoria.</li>
</ul>
</section>

<section>
<h2>Introdução</h2>
<p>
A escassez é o problema central da Economia. As necessidades humanas são numerosas, mas os recursos disponíveis são limitados.
</p>

<div class="destaque">
<strong>Ideia principal:</strong><br>
Como não podemos ter tudo ao mesmo tempo, somos obrigados a escolher. E toda a escolha tem um custo.
</div>
</section>

<section>
<h2>O que é a Escassez?</h2>
<p>
Escassez significa que os recursos existentes não são suficientes para satisfazer todas as necessidades humanas ao mesmo tempo.
</p>

<p>
Escassez não significa ausência total. Significa limitação.
</p>

<ul>
<li>Uma pessoa pode ter dinheiro, mas não o suficiente para tudo o que deseja.</li>
<li>Um país pode ter água, mas não para todos os usos possíveis.</li>
<li>Todos temos apenas 24 horas por dia.</li>
</ul>

<div class="destaque">
<strong>Fórmula económica:</strong><br>
Necessidades ilimitadas + recursos limitados = escassez.
</div>
</section>

<section>
<h2>Porque a Escassez Obriga à Escolha</h2>
<p>
Quando os recursos são limitados, pessoas, empresas e Estados têm de escolher como os utilizar.
</p>

<h3>Famílias</h3>
<ul>
<li>Comprar casa ou arrendar.</li>
<li>Poupar ou consumir.</li>
<li>Trabalhar horas extra ou descansar.</li>
</ul>

<h3>Empresas</h3>
<ul>
<li>Contratar trabalhadores ou investir em máquinas.</li>
<li>Produzir mais barato ou com maior qualidade.</li>
<li>Expandir o negócio ou reduzir risco.</li>
</ul>

<h3>Estado</h3>
<ul>
<li>Investir em hospitais ou estradas.</li>
<li>Aumentar apoios sociais ou reduzir impostos.</li>
<li>Financiar educação, defesa, saúde ou justiça.</li>
</ul>
</section>

<section>
<h2>Escolha Económica</h2>
<p>
Escolher significa selecionar uma alternativa e abdicar de outras.
</p>

<p>
Mesmo não agir é uma escolha. Adiar uma decisão também pode ter consequências económicas.
</p>

<div class="exemplo">
<strong>Exemplo:</strong><br>
Se uma pessoa decide não investir numa formação, pode poupar dinheiro no presente, mas perder oportunidades futuras.
</div>
</section>

<section>
<h2>Custo de Oportunidade</h2>
<p>
O custo de oportunidade é o valor da melhor alternativa sacrificada quando se toma uma decisão.
</p>

<p>
Não é apenas dinheiro. Pode incluir tempo, descanso, experiência, rendimento futuro ou oportunidades profissionais.
</p>

<div class="exemplo">
<strong>Exemplo prático:</strong><br>
Se usas duas horas para estudar, o custo de oportunidade pode ser descanso, lazer, trabalho remunerado ou tempo em família.
</div>
</section>

<section>
<h2>Exemplos do Quotidiano</h2>
<ul>
<li>Gastar £100 em roupa em vez de poupar.</li>
<li>Comprar um carro em vez de investir num curso.</li>
<li>Fazer horas extra em vez de descansar.</li>
<li>Arrendar casa perto do trabalho pagando mais renda.</li>
<li>Estudar à noite em vez de ver televisão.</li>
</ul>
</section>

<section>
<h2>Ligação ao Direito</h2>
<p>
As normas jurídicas influenciam escolhas económicas.
</p>

<ul>
<li>Impostos influenciam consumo e investimento.</li>
<li>Leis laborais influenciam contratação.</li>
<li>Regras de arrendamento influenciam habitação.</li>
<li>Direito comercial influencia empresas.</li>
<li>Regras de insolvência afetam decisões de risco.</li>
</ul>
</section>

<section>
<h2>Aplicação à Solicitadoria</h2>
<p>
A Solicitadoria lida com decisões jurídicas que têm impacto económico.
</p>

<ul>
<li>Comprar ou vender imóvel.</li>
<li>Aceitar ou recusar uma herança.</li>
<li>Constituir uma sociedade.</li>
<li>Assinar contrato de arrendamento.</li>
<li>Negociar uma dívida.</li>
</ul>

<div class="destaque">
<strong>Importância profissional:</strong><br>
Compreender custo de oportunidade ajuda a perceber melhor as decisões dos clientes e os efeitos práticos dos atos jurídicos.
</div>
</section>

<section>
<h2>Erros Frequentes</h2>
<ul>
<li>Pensar que escassez significa falta absoluta.</li>
<li>Achar que só o dinheiro tem valor económico.</li>
<li>Ignorar o valor do tempo.</li>
<li>Esquecer que toda a escolha implica renúncia.</li>
<li>Confundir preço com custo de oportunidade.</li>
</ul>
</section>

<section>
<h2>Pergunta de Exame</h2>
<div class="exemplo">
<strong>Pergunta:</strong><br>
Explique o conceito de custo de oportunidade e apresente dois exemplos da vida quotidiana.
</div>
</section>

<section>
<h2>Caso Prático</h2>
<div class="exemplo">
<p>
Maria dispõe de £5.000. Pode comprar um automóvel usado ou investir numa formação profissional.
</p>

<h3>Questões</h3>
<ol>
<li>Que recurso escasso possui Maria?</li>
<li>Qual é o custo de oportunidade de comprar o automóvel?</li>
<li>Qual é o custo de oportunidade de investir na formação?</li>
<li>Existe uma resposta economicamente correta?</li>
</ol>

<h3>Resposta orientadora</h3>
<p>
O recurso escasso é o dinheiro. Ao comprar o automóvel, Maria abdica da formação. Ao investir na formação, abdica do automóvel. A melhor decisão depende das necessidades, objetivos e benefícios esperados.
</p>
</div>
</section>

<section>
<h2>Resumo em 5 Pontos</h2>
<ol>
<li>A escassez é o problema económico fundamental.</li>
<li>Recursos limitados obrigam à escolha.</li>
<li>Toda a escolha implica renúncia.</li>
<li>O custo de oportunidade é a melhor alternativa sacrificada.</li>
<li>Economia e Direito influenciam decisões pessoais, empresariais e públicas.</li>
</ol>
</section>

<section class="quiz">
<h2>Mini-Quiz</h2>
<ol>
<li>O que significa escassez?</li>
<li>Porque a escassez obriga à escolha?</li>
<li>O que é custo de oportunidade?</li>
<li>Dá um exemplo de custo de oportunidade na vida de um estudante.</li>
<li>Porque este conceito interessa à Solicitadoria?</li>
</ol>
</section>

<section class="respostas">
<h2>Respostas Comentadas</h2>
<ol>
<li>Significa que os recursos são limitados face às necessidades humanas.</li>
<li>Porque não é possível satisfazer tudo ao mesmo tempo.</li>
<li>É o valor da melhor alternativa sacrificada ao escolher.</li>
<li>Estudar pode implicar abdicar de descanso, lazer ou trabalho remunerado.</li>
<li>Porque muitas decisões jurídicas envolvem custos, escolhas, património, contratos e alternativas económicas.</li>
</ol>
</section>

