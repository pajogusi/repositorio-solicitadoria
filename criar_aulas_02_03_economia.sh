<h1>Aula 02 - Necessidades, Bens e Recursos</h1>

<section class="box">
<h2>Objetivos da Aula</h2>
<ul>
<li>Distinguir necessidades humanas.</li>
<li>Identificar diferentes tipos de bens.</li>
<li>Compreender o conceito de recursos económicos.</li>
<li>Relacionar necessidades, bens e escassez.</li>
<li>Aplicar estes conceitos ao Direito e à Solicitadoria.</li>
</ul>
</section>

<section>
<h2>Introdução</h2>
<p>
A atividade económica começa com necessidades humanas. As pessoas precisam de alimentação, habitação, saúde, segurança, educação, transporte e muitas outras coisas.
</p>

<p>
Para satisfazer essas necessidades, usam bens e recursos. O problema é que esses recursos são limitados.
</p>

<div class="destaque">
<strong>Ideia principal:</strong><br>
A Economia existe porque as necessidades humanas são numerosas e os recursos disponíveis são escassos.
</div>
</section>

<section>
<h2>Necessidades Humanas</h2>
<p>
Uma necessidade é uma sensação de falta que leva a pessoa a procurar meios para a satisfazer.
</p>

<h3>Necessidades primárias</h3>
<p>São essenciais à sobrevivência e dignidade básica.</p>
<ul>
<li>Alimentação.</li>
<li>Água.</li>
<li>Habitação.</li>
<li>Saúde.</li>
<li>Segurança.</li>
</ul>

<h3>Necessidades secundárias</h3>
<p>Melhoram conforto, qualidade de vida ou realização pessoal.</p>
<ul>
<li>Lazer.</li>
<li>Viagens.</li>
<li>Entretenimento.</li>
<li>Tecnologia.</li>
<li>Formação complementar.</li>
</ul>

<h3>Necessidades individuais e coletivas</h3>
<p>
As necessidades individuais dizem respeito a uma pessoa. As coletivas dizem respeito à sociedade.
</p>

<div class="exemplo">
<strong>Exemplo:</strong><br>
Comprar alimentos é uma necessidade individual. Ter segurança pública, hospitais e estradas é uma necessidade coletiva.
</div>
</section>

<section>
<h2>Bens Económicos</h2>
<p>
Os bens são meios capazes de satisfazer necessidades humanas.
</p>

<h3>Bens livres</h3>
<p>
Existem em abundância e não exigem normalmente pagamento direto.
</p>
<ul>
<li>Ar.</li>
<li>Luz solar.</li>
</ul>

<h3>Bens económicos</h3>
<p>
São escassos, têm valor económico e exigem recursos para serem obtidos.
</p>
<ul>
<li>Alimentos.</li>
<li>Habitação.</li>
<li>Energia.</li>
<li>Transportes.</li>
<li>Serviços profissionais.</li>
</ul>

<h3>Bens materiais e imateriais</h3>
<ul>
<li><strong>Materiais:</strong> casa, carro, computador.</li>
<li><strong>Imateriais:</strong> consulta jurídica, educação, serviços de saúde.</li>
</ul>

<h3>Bens duradouros e não duradouros</h3>
<ul>
<li><strong>Duradouros:</strong> automóvel, mobiliário, imóvel.</li>
<li><strong>Não duradouros:</strong> alimentos, combustível, medicamentos consumidos rapidamente.</li>
</ul>
</section>

<section>
<h2>Recursos Económicos</h2>
<p>
Os recursos económicos são os meios utilizados para produzir bens e serviços.
</p>

<h3>Terra</h3>
<p>Inclui recursos naturais, terrenos, água, minerais e energia.</p>

<h3>Trabalho</h3>
<p>Corresponde ao esforço humano físico e intelectual.</p>

<h3>Capital</h3>
<p>Inclui máquinas, ferramentas, edifícios, tecnologia e equipamentos produtivos.</p>

<h3>Conhecimento e empreendedorismo</h3>
<p>
Representa a capacidade de organizar recursos, inovar, assumir riscos e criar valor.
</p>
</section>

<section>
<h2>Ligação à Escassez</h2>
<p>
As necessidades humanas tendem a ser ilimitadas, mas os recursos são limitados. Esta tensão cria o problema económico fundamental.
</p>

<div class="destaque">
<strong>Fórmula simples:</strong><br>
Necessidades numerosas + recursos limitados = necessidade de escolha.
</div>
</section>

<section>
<h2>Aplicação ao Direito e à Solicitadoria</h2>
<p>
Muitos bens e recursos são objeto de relações jurídicas.
</p>

<ul>
<li>Imóveis podem ser comprados, vendidos, arrendados ou herdados.</li>
<li>Empresas utilizam trabalho, capital e recursos naturais.</li>
<li>Contratos regulam trocas de bens e serviços.</li>
<li>Heranças envolvem património e bens económicos.</li>
<li>Dívidas e garantias estão ligadas a recursos e obrigações.</li>
</ul>

<div class="destaque">
<strong>Importância profissional:</strong><br>
Um solicitador lida com bens, património, contratos, empresas e direitos económicos. Por isso, precisa de compreender estes conceitos básicos.
</div>
</section>

<section>
<h2>Pergunta de Exame</h2>
<div class="exemplo">
<strong>Pergunta:</strong><br>
Distinga necessidades primárias e secundárias, apresentando dois exemplos de cada uma.
</div>
</section>

<section>
<h2>Caso Prático</h2>
<div class="exemplo">
<p>
Carlos tem £10.000. Pode comprar um automóvel usado ou investir numa formação profissional.
</p>

<h3>Questões</h3>
<ol>
<li>Que necessidades pretende satisfazer?</li>
<li>Que recurso possui?</li>
<li>Que bem ou serviço está em causa em cada opção?</li>
<li>Que escolha económica terá de fazer?</li>
</ol>

<h3>Resposta orientadora</h3>
<p>
Carlos possui dinheiro como recurso limitado. O automóvel satisfaz necessidades de transporte; a formação satisfaz uma necessidade de qualificação. Como não pode escolher tudo ao mesmo tempo, terá de comparar benefícios e custos.
</p>
</div>
</section>

<section>
<h2>Resumo em 5 Pontos</h2>
<ol>
<li>As necessidades humanas originam a atividade económica.</li>
<li>Os bens satisfazem necessidades.</li>
<li>Os bens económicos são escassos e têm valor.</li>
<li>Os recursos económicos incluem terra, trabalho, capital e conhecimento.</li>
<li>A escassez obriga à escolha.</li>
</ol>
</section>

<section class="quiz">
<h2>Mini-Quiz</h2>
<ol>
<li>O que é uma necessidade humana?</li>
<li>Qual a diferença entre bens livres e bens económicos?</li>
<li>Dá dois exemplos de bens imateriais.</li>
<li>Quais são os principais recursos económicos?</li>
<li>Porque esta matéria é importante para a Solicitadoria?</li>
</ol>
</section>

<section class="respostas">
<h2>Respostas Comentadas</h2>
<ol>
<li>É uma sensação de falta que leva à procura de meios para a satisfazer.</li>
<li>Bens livres existem em abundância; bens económicos são escassos e têm valor económico.</li>
<li>Educação, consulta jurídica, serviços médicos ou serviços financeiros.</li>
<li>Terra, trabalho, capital, conhecimento e empreendedorismo.</li>
<li>Porque a Solicitadoria trabalha com bens, património, contratos, empresas, heranças e obrigações.</li>
</ol>
</section>

