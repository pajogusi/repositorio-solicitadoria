<h1>Aula 01 - O que é a Constituição?</h1>

<section class="box">
<h2>Objetivos da Aula</h2>
<ul>
<li>Definir Constituição.</li>
<li>Compreender a sua função no Estado.</li>
<li>Perceber porque a Constituição está acima das restantes leis.</li>
<li>Identificar matérias reguladas pela Constituição.</li>
<li>Relacionar Constituição, Estado e direitos fundamentais.</li>
</ul>
</section>

<section>
<h2>Introdução</h2>
<p>
A Constituição é a lei fundamental de um Estado. Não é uma lei qualquer. É a base sobre a qual todo o sistema jurídico se organiza.
</p>

<div class="destaque">
<strong>Ideia principal:</strong><br>
A Constituição define as regras fundamentais do Estado, limita o poder político e protege os direitos dos cidadãos.
</div>
</section>

<section>
<h2>O que é a Constituição?</h2>
<p>
A Constituição é o conjunto de normas jurídicas fundamentais que organizam o Estado, definem os seus órgãos principais e consagram os direitos fundamentais das pessoas.
</p>

<p>
Em Portugal, a Constituição da República Portuguesa é a norma superior do ordenamento jurídico.
</p>
</section>

<section>
<h2>Porque é tão importante?</h2>
<ul>
<li>Organiza o poder político.</li>
<li>Define os órgãos de soberania.</li>
<li>Limita a atuação do Estado.</li>
<li>Protege direitos fundamentais.</li>
<li>Serve de referência para todas as outras leis.</li>
</ul>
</section>

<section>
<h2>Hierarquia Constitucional</h2>
<p>
Nenhuma lei, decreto-lei, regulamento ou ato administrativo pode contrariar a Constituição.
</p>

<div class="destaque">
<strong>Regra essencial:</strong><br>
Uma norma inferior que contrarie a Constituição pode ser considerada inconstitucional.
</div>
</section>

<section>
<h2>O que regula a Constituição?</h2>
<ul>
<li>Princípios fundamentais do Estado.</li>
<li>Direitos, liberdades e garantias.</li>
<li>Direitos económicos, sociais e culturais.</li>
<li>Organização do poder político.</li>
<li>Competências dos órgãos de soberania.</li>
<li>Fiscalização da constitucionalidade.</li>
</ul>
</section>

<section>
<h2>Constituição e Estado de Direito</h2>
<p>
Num Estado de Direito, o poder político não pode agir livremente sem limites. O Estado está sujeito à Constituição e à lei.
</p>

<p>
Isto significa que governantes, administração pública, tribunais e cidadãos estão todos inseridos numa ordem jurídica.
</p>
</section>

<section>
<h2>Aplicação à Solicitadoria</h2>
<p>
O solicitador não trabalha apenas com contratos, registos ou documentos. Trabalha dentro de uma ordem jurídica que tem a Constituição no topo.
</p>

<ul>
<li>Direitos fundamentais podem influenciar casos concretos.</li>
<li>Leis e regulamentos devem respeitar a Constituição.</li>
<li>Atos administrativos podem ser questionados quando violam princípios constitucionais.</li>
<li>A proteção da propriedade, igualdade e acesso ao Direito são matérias constitucionais relevantes.</li>
</ul>
</section>

<section>
<h2>Erros Frequentes</h2>
<ul>
<li>Pensar que a Constituição é apenas simbólica.</li>
<li>Confundir Constituição com uma lei comum.</li>
<li>Ignorar que todas as normas inferiores devem respeitá-la.</li>
<li>Pensar que Direito Constitucional só interessa aos políticos.</li>
</ul>
</section>

<section>
<h2>Resumo em 5 Pontos</h2>
<ol>
<li>A Constituição é a lei fundamental do Estado.</li>
<li>Organiza o poder político.</li>
<li>Protege direitos fundamentais.</li>
<li>Está acima das restantes normas jurídicas.</li>
<li>É essencial para compreender o Estado de Direito.</li>
</ol>
</section>

<section class="quiz">
<h2>Mini-Quiz</h2>
<ol>
<li>O que é a Constituição?</li>
<li>Porque é considerada a lei fundamental?</li>
<li>O que acontece se uma lei contrariar a Constituição?</li>
<li>Que matérias são reguladas pela Constituição?</li>
<li>Porque esta matéria é importante para a Solicitadoria?</li>
</ol>
</section>

<section class="respostas">
<h2>Respostas Comentadas</h2>
<ol>
<li>É o conjunto de normas fundamentais que organiza o Estado e consagra direitos fundamentais.</li>
<li>Porque ocupa o topo da hierarquia jurídica e serve de base ao sistema jurídico.</li>
<li>Pode ser considerada inconstitucional.</li>
<li>Direitos fundamentais, organização do poder político, órgãos de soberania e princípios do Estado.</li>
<li>Porque toda a atividade jurídica decorre dentro de uma ordem constitucional.</li>
</ol>
</section>

