<section class="respostas">
<h2>Respostas Comentadas</h2>
<ol>
<li>Estuda a utilização de recursos escassos para satisfazer necessidades humanas.</li>
<li>Significa que os recursos são limitados face às necessidades existentes.</li>
<li>É o valor da melhor alternativa sacrificada quando se toma uma decisão.</li>
<li>Famílias, empresas, Estado ou resto do mundo.</li>
<li>Consomem, trabalham, recebem rendimento, poupam e pagam impostos.</li>
<li>Produzem bens e serviços, contratam trabalho, investem e pagam impostos.</li>
<li>É a criação de bens ou serviços.</li>
<li>É a utilização de bens e serviços para satisfazer necessidades.</li>
<li>Salário, pensão, rendas, lucros, juros ou subsídios.</li>
<li>É o contexto onde compradores e vendedores realizam trocas.</li>
<li>Quantidade que consumidores querem e podem comprar.</li>
<li>Quantidade que vendedores estão dispostos a disponibilizar.</li>
<li>O preço tende a subir.</li>
<li>Disputa entre vendedores ou produtores pelos consumidores.</li>
<li>Ponto em que procura e oferta coincidem.</li>
<li>Regulação, prestação de serviços, redistribuição ou estabilização.</li>
<li>Financiar serviços públicos e funções do Estado.</li>
<li>Porque leis, impostos, contratos e regulação alteram decisões económicas.</li>
<li>Porque contratos, imóveis, empresas, dívidas, heranças e impostos têm dimensão económica.</li>
