<section>
<h2>O Caso</h2>
<div class="exemplo">
<p>
João pretende abrir uma pequena empresa de prestação de serviços de manutenção de jardins.
Tem £8.000 disponíveis. Pode usar esse dinheiro para comprar equipamentos, criar publicidade, pagar seguros, registar a atividade e contratar ajuda ocasional.
</p>

<p>
Ao mesmo tempo, João tem uma proposta para continuar empregado por conta de outrem com salário fixo.
</p>
</div>
</section>

<section>
<h2>Análise Económica</h2>
<ul>
<li><strong>Recurso escasso:</strong> dinheiro, tempo e capacidade de trabalho.</li>
<li><strong>Escolha:</strong> abrir empresa ou manter emprego.</li>
<li><strong>Custo de oportunidade:</strong> rendimento estável que João pode perder ao criar negócio próprio.</li>
<li><strong>Mercado:</strong> procura por serviços de jardinagem na zona.</li>
<li><strong>Preço:</strong> deve cobrir custos e permitir lucro.</li>
<li><strong>Estado:</strong> impostos, regras legais, segurança social e licenças aplicáveis.</li>
</ul>
</section>

<section>
<h2>Questões Orientadoras</h2>
<ol>
<li>Que agentes económicos aparecem no caso?</li>
<li>Que recursos João possui?</li>
<li>Qual é o custo de oportunidade da decisão?</li>
<li>Que fatores influenciam o preço dos serviços?</li>
<li>Que áreas jurídicas podem estar envolvidas?</li>
</ol>
</section>

<section>
<h2>Resposta Orientadora</h2>
<p>
Estão envolvidos João, possíveis clientes, fornecedores, Estado e eventualmente trabalhadores. A decisão envolve escassez de recursos, análise de procura, risco económico e cumprimento de obrigações legais.
</p>

<p>
Do ponto de vista jurídico, podem estar envolvidos contratos, fiscalidade, segurança social, responsabilidade civil, atividade empresarial e eventual Direito do Trabalho.
</p>
</section>

