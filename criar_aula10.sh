<section>
<h2>Revisão Geral</h2>

<p>
Nesta cadeira estudámos os fundamentos essenciais de Introdução ao Direito I.
</p>

<p>
O objetivo não foi decorar leis, mas compreender a lógica base do sistema jurídico.
</p>

<div class="destaque">
<strong>Ideia principal:</strong><br>
Antes de estudar ramos específicos do Direito, é necessário compreender o que é o Direito, como surgem as normas, como se interpretam e como são aplicadas.
</div>
</section>

<section>
<h2>Aula 01 — O que é o Direito?</h2>
<ul>
<li>O Direito regula a vida em sociedade.</li>
<li>Define direitos, deveres e consequências.</li>
<li>Pode ser imposto pelo Estado.</li>
<li>Distingue-se entre Direito Objetivo e Direito Subjetivo.</li>
</ul>
</section>

<section>
<h2>Aula 02 — Direito, Moral, Ética e Justiça</h2>
<ul>
<li>Direito, Moral, Ética e Justiça são conceitos relacionados, mas diferentes.</li>
<li>Nem tudo o que é legal é moral.</li>
<li>Nem tudo o que é moral é legal.</li>
<li>A Ética é essencial nas profissões jurídicas.</li>
</ul>
</section>

<section>
<h2>Aula 03 — A Norma Jurídica</h2>
<ul>
<li>A norma jurídica é a unidade básica do Direito.</li>
<li>Tem generalidade, abstração, imperatividade, coercibilidade e bilateralidade.</li>
<li>A norma pode apresentar previsão e estatuição.</li>
</ul>
</section>

<section>
<h2>Aula 04 — Fontes do Direito</h2>
<ul>
<li>As fontes explicam a origem das normas jurídicas.</li>
<li>A lei é a principal fonte do Direito Português.</li>
<li>Jurisprudência e doutrina ajudam na interpretação.</li>
<li>A Constituição ocupa o topo da hierarquia normativa.</li>
</ul>
</section>

<section>
<h2>Aula 05 — Ramos do Direito</h2>
<ul>
<li>O Direito divide-se em ramos para facilitar estudo e aplicação.</li>
<li>Direito Público envolve o Estado em posição de autoridade.</li>
<li>Direito Privado regula relações entre particulares.</li>
<li>A Solicitadoria cruza vários ramos jurídicos.</li>
</ul>
</section>

<section>
<h2>Aula 06 — Estado, Constituição e Lei</h2>
<ul>
<li>O Estado possui povo, território e poder político.</li>
<li>A Constituição é a lei fundamental.</li>
<li>A lei deve respeitar a Constituição.</li>
<li>No Estado de Direito, o próprio Estado está sujeito ao Direito.</li>
</ul>
</section>

<section>
<h2>Aula 07 — Tribunais e Aplicação do Direito</h2>
<ul>
<li>Os tribunais aplicam o Direito aos casos concretos.</li>
<li>A função jurisdicional resolve litígios.</li>
<li>Aplicar o Direito exige factos, normas e prova.</li>
<li>A independência dos tribunais é essencial.</li>
</ul>
</section>

<section>
<h2>Aula 08 — Interpretação da Lei</h2>
<ul>
<li>Interpretar é descobrir o sentido e alcance da norma.</li>
<li>A letra da lei é importante, mas não basta.</li>
<li>Existem métodos literal, histórico, sistemático e teleológico.</li>
<li>A interpretação correta garante segurança jurídica.</li>
</ul>
</section>

<section>
<h2>Aula 09 — Caso Prático Orientado</h2>
<ul>
<li>A análise jurídica começa pelos factos relevantes.</li>
<li>Depois identifica-se o ramo do Direito aplicável.</li>
<li>É necessário distinguir factos, opiniões, moral e Direito.</li>
<li>A solução depende da prova, documentos e normas aplicáveis.</li>
</ul>
</section>

<section class="quiz">
<h2>Quiz Final</h2>

<p>
Responde antes de consultar as soluções. Este quiz serve para verificar se compreendeste a cadeira.
</p>

<ol>
