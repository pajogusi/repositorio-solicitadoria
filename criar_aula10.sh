<li>Qual a principal diferença entre Direito e Moral?</li>
<li>O que é a Ética?</li>
<li>O que representa a Justiça?</li>
<li>O que é uma norma jurídica?</li>
<li>Indica três características da norma jurídica.</li>
<li>Qual a diferença entre previsão e estatuição?</li>
<li>O que são fontes do Direito?</li>
<li>Qual é a principal fonte do Direito Português?</li>
<li>Qual a função da jurisprudência?</li>
<li>Qual a diferença entre Direito Público e Direito Privado?</li>
<li>Dá dois exemplos de ramos do Direito Privado.</li>
<li>Quais são os três elementos essenciais do Estado?</li>
<li>O que é a Constituição?</li>
<li>O que significa hierarquia das normas?</li>
<li>Qual é a função principal dos tribunais?</li>
<li>O que significa aplicar o Direito ao caso concreto?</li>
<li>Quais são os principais métodos de interpretação da lei?</li>
<li>Porque é importante distinguir factos de opiniões num caso jurídico?</li>
</ol>
</section>

<section class="respostas">
<h2>Respostas Comentadas</h2>

<ol>
<li>É o conjunto de normas jurídicas que regulam a vida em sociedade e que podem ser impostas pelo Estado.</li>
<li>Direito Objetivo é o conjunto das normas; Direito Subjetivo é o poder reconhecido a uma pessoa concreta.</li>
<li>O Direito pode ser imposto pelo Estado; a Moral resulta de valores sociais ou pessoais sem sanção estatal direta.</li>
<li>É a reflexão crítica sobre a Moral e sobre aquilo que consideramos certo ou errado.</li>
<li>É um ideal de equilíbrio, equidade e respeito pelo que é devido a cada pessoa.</li>
<li>É uma regra de conduta criada ou reconhecida pelo Estado.</li>
<li>Generalidade, abstração, imperatividade, coercibilidade e bilateralidade.</li>
<li>A previsão descreve a situação; a estatuição define a consequência jurídica.</li>
<li>São os meios através dos quais surgem ou são reconhecidas normas jurídicas.</li>
<li>A lei.</li>
<li>Ajuda a interpretar e aplicar as normas jurídicas através das decisões dos tribunais.</li>
<li>Direito Público envolve o Estado em posição de autoridade; Direito Privado regula relações entre particulares.</li>
<li>Direito Civil, Direito Comercial, Direito das Obrigações, Direito das Coisas ou Família e Sucessões.</li>
<li>Povo, território e poder político.</li>
<li>É a lei fundamental do Estado.</li>
<li>Significa que normas inferiores não podem contrariar normas superiores.</li>
<li>Resolver conflitos jurídicos e aplicar o Direito através de decisões obrigatórias.</li>
<li>Analisar factos, identificar normas aplicáveis e retirar consequências jurídicas.</li>
<li>Literal, histórico, sistemático e teleológico.</li>
<li>Porque o Direito decide com base em factos, prova e normas, não apenas em sentimentos ou opiniões.</li>
</ol>
</section>

